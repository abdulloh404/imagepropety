            ขณะที่ประเด็นสายพันธุ์ย่อยลงมาของโอมิครอนอย่าง BA.2.2 และ BA.2.3 ตอนนี้ทางกรมวิทย์ฯ ตรวจตัวอย่างจากกลุ่มผู้ติดเชื้อในประเทศและต่างประเทศแบบถอดรหัสพันธุกรรมทั้งตัวกว่า 500 ตัวอย่างทุกสัปดาห์ พบสายพันธุ์ย่อย BA.2.2 จำนวน 22 ราย และสายพันธุ์ย่อย BA.2.3 จำนวน 61 ราย รวม 2 สายพันธุ์พบ 83 ราย<br>
